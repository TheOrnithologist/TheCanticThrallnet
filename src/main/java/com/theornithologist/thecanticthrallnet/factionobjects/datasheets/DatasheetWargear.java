package com.theornithologist.thecanticthrallnet.factionobjects.datasheets;

public class DatasheetWargear {

    String datasheetsID;
    String line;
    String dice;
    String name;
    String description;
    String range;
    String type;
    String attacks;
    String attackSkill;
    String strength;
    String armorPiercing;
    String damage;


    public String getDatasheetsID() {
        return datasheetsID;
    }

    public String getLine() {
        return line;
    }

    public String getDice() {
        return dice;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getRange() {
        return range;
    }

    public String getType() {
        return type;
    }

    public String getAttacks() {
        return attacks;
    }

    public String getAttackSkill() {
        return attackSkill;
    }

    public String getStrength() {
        return strength;
    }

    public String getArmorPiercing() {
        return armorPiercing;
    }

    public String getDamage() {
        return damage;
    }
}
